-- 16 AUDIT
CREATE TABLE SDB_AUDITABLE (
    AUD_ID INT NOT NULL AUTO_INCREMENT,
    AUD_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    AUD_MODIFIED_BY VARCHAR(64),
    AUD_TABLE_AFFECTED VARCHAR(100),
